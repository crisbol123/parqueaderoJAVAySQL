    private void iniciarLista(){
        this.vehiculos=this.adao.VerVehiculo();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for(int i = 0; i< this.vehiculos.size();i++){
            dtm.addRow(new Object[]{
               this.vehiculos.get(i).getPlaca(),
                this.vehiculos.get(i).getNombreUsuario(),
                this.vehiculos.get(i).getBahia(),
                this.vehiculos.get(i).getEnumVehiculo(),
                this.vehiculos.get(i).getHoraEntrada(),
                this.vehiculos.get(i).getEstado()
            
            });
        }
        
    }
    
