    private void iniciarLista(){
        this.vigilantes=this.adao.VerVigilante();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for(int i = 0; i< this.vigilantes.size();i++){
            dtm.addRow(new Object[]{
               this.vigilantes.get(i).getIdVigilante(),
                this.vigilantes.get(i).getNombre(),
                this.vigilantes.get(i).getApellido(),
                this.vigilantes.get(i).getCorreo(),
                this.vigilantes.get(i).getIdAdmin(),
                this.vigilantes.get(i).getCelular()

            });
        }
       
    }
