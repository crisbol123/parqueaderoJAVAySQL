    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
              // TODO add your handling code here:ELIMINAR
        int fila_seleccionada = jTable1.getSelectedRow();
        if(fila_seleccionada >= 0){            
            int decision = JOptionPane.showConfirmDialog(null, "Confirma que desea eliminar este vigilante?", "Advertencia", JOptionPane.YES_NO_OPTION);            
            if(decision == 0){
                    Vigilante VigAEliminar = this.vigilantes.get(fila_seleccionada);
                    this.adao.EliminarVigilante(VigAEliminar);
                    this.iniciarLista();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione una fila.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

