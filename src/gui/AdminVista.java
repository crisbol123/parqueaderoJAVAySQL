    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
              // TODO add your handling code here:ELIMINAR
        int fila_seleccionada = jTable1.getSelectedRow();
        if(fila_seleccionada >= 0){            
            int decision = JOptionPane.showConfirmDialog(null, "Confirma que desea eliminar este admin?", "Advertencia", JOptionPane.YES_NO_OPTION);            
            if(decision == 0){
                    Administrador AdminAEliminar = this.administradores.get(fila_seleccionada);
                    this.adao.EliminarAdministrador(AdminAEliminar);
                    this.iniciarLista();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Seleccione una fila.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

